    public void enablebutton_table(int id)
    {
        if (id == 1)
        {
            Button_Table1.setEnabled(true);
        } else if (id == 2)
        {
            Button_Table2.setEnabled(true);
        } else if (id == 3)
        {
            Button_Table3.setEnabled(true);
        } else
        {
            Button_Table4.setEnabled(true);
        }
    }

    public void disablebutton_table(int id)
    {
        if (id == 1)
        {
            Button_Table1.setEnabled(false);
        } else if (id == 2)
        {
            Button_Table2.setEnabled(false);
        } else if (id == 3)
        {
            Button_Table3.setEnabled(false);
        } else
        {
            Button_Table4.setEnabled(false);
        }
    }

    public void InsertProduct(int id_table, int product_id, int quantity)
    {        //danh dau ban da dat: 1(bantrong) 0(dadat)
        if (trangthai_table[id_table - 1] == 1)
        {
            enablebutton_table(id_table);
            try
            {
                String dbURL = "jdbc:sqlserver://localhost:1433;databaseName=fastfood;integratedSecurity=true";
                Connection conn = DriverManager.getConnection(dbURL);
                String query = "update ban set trangthai = 0 where id_ban = ?";
                PreparedStatement pst = conn.prepareStatement(query);
                pst.setInt(1, id_table);
                pst.executeUpdate();
            } catch (SQLException ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
            trangthai_table[id_table - 1] = 0;

            //insert vao table Bill lan` dau` dung` ban`
            try
            {
                String dbURL = "jdbc:sqlserver://localhost:1433;databaseName=fastfood;integratedSecurity=true";
                Connection conn = DriverManager.getConnection(dbURL);
                String query = "insert into Bill(FK_id_table, FK_id_nhanvien) values (?,?)";
                PreparedStatement pst = conn.prepareStatement(query);
                pst.setInt(1, id_table);
                pst.setInt(2, this.current_user_id);
                pst.executeUpdate();
            } catch (SQLException ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
        }
        try
        {
            String dbURL = "jdbc:sqlserver://localhost:1433;databaseName=fastfood;integratedSecurity=true";
            Connection conn = DriverManager.getConnection(dbURL);
            String query = "insert into custom_order(fk_id_ban, fk_id_sp, soluong, ngay) values (?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setInt(1, id_table);
            pst.setInt(2, product_id);
            pst.setInt(3, quantity);
            pst.setDate(4, java.sql.Date.valueOf(LocalDate.now()));
            pst.executeUpdate();
        } catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        try
        {
            String dbURL = "jdbc:sqlserver://localhost:1433;databaseName=fastfood;integratedSecurity=true";
            Connection conn = DriverManager.getConnection(dbURL);
            String query = "insert into thongke(fk_id_sp, soluong, ngay) values (?,?,?)";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setInt(1, product_id);
            pst.setInt(2, quantity);
            pst.setDate(3, java.sql.Date.valueOf(LocalDate.now()));
            pst.executeUpdate();
        } catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }

