    public void showbill_table(int id)
    {
        Label_BillOf.setText("Bill Of Table" + Integer.toString(id));
        int[] soluongsp = new int[ComboBox_ProductSelect.getItemCount() + 1];
        String[] tensp = new String[ComboBox_ProductSelect.getItemCount() + 1];
        int[] giasp = new int[ComboBox_ProductSelect.getItemCount() + 1];
        for (int i = 1; i < soluongsp.length; i++)
        {
            soluongsp[i] = 0;
            tensp[i] = "";
            giasp[i] = 0;
        }
        try
        {
            String dbURL = "jdbc:sqlserver://localhost:1433;databaseName=fastfood;integratedSecurity=true";
            Connection conn = DriverManager.getConnection(dbURL);
            String query = "select * from custom_order where fk_id_ban = ?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            while (rs.next())
            {
                soluongsp[rs.getInt("fk_id_sp")] += rs.getInt("soluong");
            }
        } catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        try
        {
            String dbURL = "jdbc:sqlserver://localhost:1433;databaseName=fastfood;integratedSecurity=true";
            Connection conn = DriverManager.getConnection(dbURL);
            String query = "select * from sanpham";
            PreparedStatement pst = conn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next())
            {
                if (soluongsp[rs.getInt("id_sp")] > 0)
                {
                    tensp[rs.getInt("id_sp")] = rs.getString("tensanpham");
                    giasp[rs.getInt("id_sp")] = rs.getInt("giathanh");
                }
            }
        } catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        ArrayList<hoadon> list = splist(tensp, soluongsp, giasp);
        DefaultTableModel model = (DefaultTableModel) Table_BillDetails.getModel();
        model.setRowCount(0);
        Object[] row = new Object[3];
        int sum = 0;
        for (int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getten();
            row[1] = list.get(i).getsoluong();
            row[2] = list.get(i).getgia();
            sum += list.get(i).getsoluong() * list.get(i).getgia();
            model.addRow(row);
        }
        Label_TotalAmount.setText(Integer.toString(sum) + "000");
        jScrollPane1.setVisible(true);
    }

    public ArrayList<hoadon> splist(String[] ten, int[] soluong, int[] gia)
    {
        ArrayList<hoadon> sp_list = new ArrayList<>();
        for (int i = 1; i < soluong.length; i++)
        {
            if (soluong[i] > 0)
            {
                hoadon _hoadon = new hoadon(ten[i], soluong[i], gia[i]);
                sp_list.add(_hoadon);
            }
        }
        return sp_list;
    }
